def compute_gini(G,N):
    degrees = list(G.degree)
    x = sorted([i[1] for i in degrees])
    if N*sum(x) !=0:
        B = sum( xi * (N-i) for i,xi in enumerate(x) ) / (N*sum(x))
        return (1 + (1/N) - 2*B)
    else:
        return "NA"

